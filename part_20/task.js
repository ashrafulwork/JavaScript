//Task _ Making a Calculator

function addition (x,y){

    var result = x+ y;
    return result;
}
function substraction (x,y){

    var result = x- y;
    return result;
}

function multiplication (x,y){

    var result = x*y;
    return result;
}

function division (x,y){

    var result = x/ y;
    return result;
}


document.write(addition(10,127)+"<br>");
document.write(substraction(148,10)+"<br>");
document.write(multiplication(5,8)+"<br>");
document.write(division(120,10)+"<br>");
